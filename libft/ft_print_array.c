#include "libft.h"

void	ft_print_array(char **arr)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (arr[i] != 0)
	{
		while (arr[i][j] != '\0')
		{
			ft_putchar(arr[i][j]);
			j++;
		}
		ft_putchar('\n');
		j = 0;
		i++;
	}
}
